export function normalizeFrontendLanguage(raw?: string | null): FrontendLanguage {
  if (raw === 'ja') return 'ja'
  return 'en'
}

export function t(lang: FrontendLanguage, key: string, params?: Record<string, string | number>): string {
  const template = dictionaries[lang]?.[key] || dictionaries.en[key] || key
  if (!params) return template
  return template.replace(/\{(\w+)\}/g, (_, token) => String(params[token] ?? `{${token}}`))
}

export function translateNavLabel(label: string, lang: FrontendLanguage): string {
  if (!label) return label
  const normalized = label.trim().toLowerCase()
  if (normalized === 'home') return t(lang, 'nav.home')
  if (normalized === 'products') return t(lang, 'nav.products')
  if (normalized === 'about') return t(lang, 'nav.about')
  if (normalized === 'contact') return t(lang, 'nav.contact')
  if (normalized === 'cart') return t(lang, 'nav.cart')
  return label
}

export function translateAttributeName(label: string, lang: FrontendLanguage): string {
  if (!label || lang !== 'ja') return label
  const key = label.replace(/\s+/g, '').toLowerCase()
  return attributeLabelMapJa[key] || label
}
